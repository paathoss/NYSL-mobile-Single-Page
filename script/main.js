const rulesP = document.getElementById("rulesPage");


function render(pagina) {
  document.getElementById('sidebar').classList.toggle('active');
  document.getElementById('blur').style.display = "none";

  indexP.style.display = "none";
  aboutP.style.display = "none";
  rulesP.style.display = "none";
  registrationP.style.display = "none";
  infoP.style.display = "none";
  contactP.style.display = "none";

  if (pagina === 'aboutP') {
    aboutP.style.display = "block";

    document.getElementById('sidebar').classList.add('sidebarA');
    document.getElementById('blur').classList.add('blurA');
  } else if (pagina === 'registrationP') {
    registrationP.style.display = "block";

    document.getElementById('sidebar').classList.add('sidebarRe');
    document.getElementById('blur').classList.add('blurRe');
  } else if (pagina === 'indexP') {
    indexP.style.display = "block";

    document.getElementById('sidebar').classList.add('sidebarIn');
    document.getElementById('blur').classList.add('blureado');
  } else if (pagina === 'infoP') {
    infoP.style.display = "block";

    document.getElementById('sidebar').classList.add('sidebarTa');
    document.getElementById('blur').classList.add('blurTa');
  } else if (pagina === 'contactP') {
    contactP.style.display = "block";

    document.getElementById('sidebar').classList.add('sidebarC');
    document.getElementById('blur').classList.add('blurC');
  } else if (pagina === 'rulesP') {
    rulesP.style.display = "block";

    document.getElementById('sidebar').classList.add('sidebarR');
    document.getElementById('blur').classList.add('blurR');
  }
}


